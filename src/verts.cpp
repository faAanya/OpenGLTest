    std::vector<Vertex> sphere(float radius, unsigned int stacks, unsigned int slices) {
        std::vector<Vertex> vertices;

        for (unsigned int i = 0; i <= stacks; ++i) {
            float phi = glm::pi<float>() * static_cast<float>(i) / static_cast<float>(stacks);
            float cosPhi = cos(phi);
            float sinPhi = sin(phi);

            for (unsigned int j = 0; j <= slices; ++j) {
                float theta = 2.0f * glm::pi<float>() * static_cast<float>(j) / static_cast<float>(slices);
                float cosTheta = cos(theta);
                float sinTheta = sin(theta);

                Vertex vertex;
                vertex.Position = glm::vec3(radius * sinPhi * cosTheta,
                                            radius * cosPhi,
                                            radius * sinPhi * sinTheta);

                vertex.Normal = glm::normalize(vertex.Position);
                vertex.texUV = glm::vec2(static_cast<float>(j) / static_cast<float>(slices),
                                             static_cast<float>(i) / static_cast<float>(stacks));

                vertices.push_back(vertex);
            }
        }

        return vertices;
    }

